#import "PriorityPicker.h"

@interface PriorityPicker ()

@end

@implementation PriorityPicker
{
    NSArray *_priorityArray;
    NSUInteger _selectedIndex;
}

- (void)viewDidLoad {
    [super viewDidLoad];
    
    _priorityArray = @[@"High",
               @"Med",
               @"Low",
               ];
    _selectedIndex = [_priorityArray indexOfObject:self.priority];
}





- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {

    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {

    return _priorityArray.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"priorCell"];
    cell.textLabel.text = _priorityArray[indexPath.row];
    
    if (indexPath.row == _selectedIndex) {
        cell.accessoryType = UITableViewCellAccessoryCheckmark;
    } else {
        cell.accessoryType = UITableViewCellAccessoryNone;
    }
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    [tableView deselectRowAtIndexPath:indexPath animated:YES];
    
    if (_selectedIndex != NSNotFound) {
        UITableViewCell *cell = [tableView cellForRowAtIndexPath:
                                 [NSIndexPath indexPathForRow:_selectedIndex inSection:0]];
        cell.accessoryType = UITableViewCellAccessoryNone;
    }
    
    _selectedIndex = indexPath.row;
    
    UITableViewCell *cell = [tableView cellForRowAtIndexPath:indexPath];
    cell.accessoryType = UITableViewCellAccessoryCheckmark;
    
    NSString *priorityString = _priorityArray[indexPath.row];
    [self.delegate priorityPickerViewController:self didSelectPriority:priorityString];
}




